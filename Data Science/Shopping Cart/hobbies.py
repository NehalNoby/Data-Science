# 2. Common Hobbies

# Create two sets of hobbies for Student A and Student B.

# Find hobbies common to both.

# Find hobbies unique to Student A.

# Find all hobbies (union).


hobbies_A={"Cricket","Football","Hockey","Badminton","Tennis"}
hobbies_B={"Swimming","Cycling","Cricket","Running","Tennis"}
print("Common Hobbies:", hobbies_A.intersection(hobbies_B))
print("Common Hobbies:", hobbies_A.difference(hobbies_B))
print("Common Hobbies:", hobbies_A.union(hobbies_B))

